package Testingpackage;

public class Relationaloperators {

	public static void main(String[] args) {
		
		int g=34;
		int h=38;
		System.out.println(g>h);
		
		System.out.println(g<h);
		
		System.out.println(g>=h);
		
		System.out.println(g<=h);
		
		System.out.println(g==h);
		
		System.out.println(g!=h);

	}

}
